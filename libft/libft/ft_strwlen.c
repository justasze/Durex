#include "libft.h"

size_t		ft_strwlen(char const *str)
{
	size_t	i;

	i = 0;
	while (*str != '\0')
	{
		if (*str >= 0)
			i++;
		else if ((*str & 224) == 192)
		{
			str++;
			i++;
		}
		else if ((*str & 240) == 224)
		{
			str += 2;
			i += 2;
		}
		else if ((*str & 248) == 240)
		{
			str += 3;
			i += 3;
		}
		str++;
	}
	return (i);
}
